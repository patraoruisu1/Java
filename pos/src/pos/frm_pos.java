    private void bt_pagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pagamentoActionPerformed
        if(list_descricao.getModel().getSize()==0){
            JOptionPane.showMessageDialog(this, 
                    "Não existem items", 
                    "Pagamento", JOptionPane.ERROR_MESSAGE);
            return;
        }
        float recebido=0;
        try{
            recebido=Float.parseFloat(JOptionPane.showInputDialog(this,
                    "Digite o valor entregue para pagamento"));
            if(recebido<total_pedido){
                JOptionPane.showMessageDialog(this, 
                        "Valor insuficiente","POS",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, 
                    "Verifique os dados","POS",
                     JOptionPane.ERROR_MESSAGE);
                return;
        }
        lbl_numerario.setText(Moeda.format(recebido));
        lbl_troco.setText(Moeda.format(recebido-total_pedido));
        
        
        Desabilitar_Paineis();
        Desabilita_Botoes();
        
        bt_imprimir.setEnabled(true);
        
    }//GEN-LAST:event_bt_pagamentoActionPerformed


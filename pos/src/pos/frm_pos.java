    private void bt_quantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_quantidadeActionPerformed
        // TODO add your handling code here:
        if(list_descricao.getSelectedIndex()<0){
            JOptionPane.showMessageDialog(this, 
            "Selecione um item","POS",
            JOptionPane.ERROR_MESSAGE);
            return;
        }
        int qtd=0;
        try{
            qtd=Integer.parseInt(JOptionPane.showInputDialog(this,
                    "Digite a quantidade"));
            if(qtd<=0){
                JOptionPane.showMessageDialog(this, 
                "Quantidade inválida","POS",
                JOptionPane.ERROR_MESSAGE);
            return;                
            }
        }
        catch(NumberFormatException e){
          JOptionPane.showMessageDialog(this, 
            "Verifique os dados","POS",
            JOptionPane.ERROR_MESSAGE);
            return;            
        }
        int posicao=list_descricao.getSelectedIndex();
        list_qtd.setSelectedIndex(posicao);
        int new_qtd=list_qtd.getSelectedValue()+qtd;
        quantidade.setElementAt(new_qtd, posicao);
        
        list_preco.setSelectedIndex(posicao);
        float prc=list_preco.getSelectedValue();
        list_total.setSelectedIndex(posicao);
        total.setElementAt(Moeda.format(new_qtd*prc), posicao);
        
        total_pedido+=(prc*qtd);
        lbl_total.setText(Moeda.format(total_pedido));
        
        list_descricao.setSelectedIndex(list_qtd.getSelectedIndex());
        list_preco.setSelectedIndex(list_qtd.getSelectedIndex());
        list_total.setSelectedIndex(list_qtd.getSelectedIndex());
    }//GEN-LAST:event_bt_quantidadeActionPerformed


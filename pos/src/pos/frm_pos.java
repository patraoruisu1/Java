    private void AdicionarProduto(String NomeProduto,float PrecoProduto){
        try{
            int posicao=list_descricao.getNextMatch(NomeProduto, 
                    0, Position.Bias.Forward);
            list_qtd.setSelectedIndex(posicao);
            int qtd=list_qtd.getSelectedValue()+1;
            quantidade.setElementAt(qtd, posicao);
            
            list_total.setSelectedIndex(posicao);
            total.setElementAt(Moeda.format(qtd*PrecoProduto), posicao);
            
            total_pedido+=PrecoProduto;
            lbl_total.setText(Moeda.format(total_pedido));
            
            list_descricao.setSelectedIndex(posicao);
            list_preco.setSelectedIndex(posicao);
            
            return;
        }
        catch(Exception e){  
        }
     
        // adicionar a descrição à lista
        descricao.addElement(NomeProduto);
        list_descricao.setModel(descricao);
        
        quantidade.addElement(1);
        list_qtd.setModel(quantidade);    
        
        preco.addElement(PrecoProduto);
        list_preco.setModel(preco);
        
        total.addElement(Moeda.format(PrecoProduto));
        list_total.setModel(total);   
        
        total_pedido+=PrecoProduto;
        lbl_total.setText(Moeda.format(total_pedido));
        Habilita_Botoes();   
    }
    
    private void bt_novoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_novoActionPerformed
        // TODO add your handling code here:
        if(bt_novo.getText().equals("Novo")){
            Calendar C=new GregorianCalendar();
            int dia=C.get(Calendar.DAY_OF_MONTH);
            int mes=C.get(Calendar.MONTH);
            int ano=C.get(Calendar.YEAR);

            int hora=C.get(Calendar.HOUR_OF_DAY);
            int minuto=C.get(Calendar.MINUTE);
            int segundo=C.get(Calendar.SECOND);

            lbl_pedido.setText(Integer.toString(dia)+
                    Integer.toString(mes)+
                    Integer.toString(ano)+
                    Integer.toString(hora)+
                    Integer.toString(minuto)+
                    Integer.toString(segundo));
            bt_novo.setText("Cancelar");
            Habilitar_Paineis();
        }
        else{
           bt_novo.setText("Novo");
           lbl_pedido.setText("");
           Desabilitar_Paineis();
           Clear_All();
        }
        
    }//GEN-LAST:event_bt_novoActionPerformed

    private void bt_adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_adicionarActionPerformed
        if(list_descricao.getSelectedIndex()<0){
            JOptionPane.showMessageDialog(this, 
                    "Não existem items selecionados", 
                    "Adicionar produto", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int posicao=list_descricao.getSelectedIndex();
        list_qtd.setSelectedIndex(posicao);
        int qtd=list_qtd.getSelectedValue()+1;
        quantidade.setElementAt(qtd,posicao);
        
        list_preco.setSelectedIndex(posicao);
        float wpreco=list_preco.getSelectedValue();
        list_total.setSelectedIndex(posicao);
        total.setElementAt(Moeda.format(qtd*wpreco),posicao);
        total_pedido+=wpreco;
        lbl_total.setText(Moeda.format(total_pedido));
    }//GEN-LAST:event_bt_adicionarActionPerformed

    private void bt_remover_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_remover_itemActionPerformed
        if(list_descricao.getSelectedIndex()<0){
            JOptionPane.showMessageDialog(this, 
                    "Não existem items selecionados", 
                    "Adicionar produto", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int posicao=list_descricao.getSelectedIndex();
        list_qtd.setSelectedIndex(posicao);
        int qtd=list_qtd.getSelectedValue()-1;
        quantidade.setElementAt(qtd,posicao);
        
        list_preco.setSelectedIndex(posicao);
        float wpreco=list_preco.getSelectedValue();
        list_total.setSelectedIndex(posicao);
        total.setElementAt(Moeda.format(qtd*wpreco),posicao);
        total_pedido-=wpreco;
        lbl_total.setText(Moeda.format(total_pedido));
        
        if(qtd==0){
            descricao.remove(list_descricao.getSelectedIndex());
            quantidade.remove(list_qtd.getSelectedIndex());
            preco.remove(list_preco.getSelectedIndex());
            total.remove(list_total.getSelectedIndex());
        }
        if(descricao.getSize()==0){
            Desabilita_Botoes();
        }
    }//GEN-LAST:event_bt_remover_itemActionPerformed

